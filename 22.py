import time
import sys

def print_slowly(text, delay=0.03):
    """Печатает текст посимвольно с задержкой."""
    for char in text:
        sys.stdout.write(char)
        sys.stdout.flush()
        time.sleep(delay)
    print()  # Перевод строки в конце

